// Generate realistic timestamps over the past 30 days
const generateRealisticTimestamp = (daysAgo: number, hoursVariation: number = 12) => {
  const now = Date.now();
  const baseDaysAgo = daysAgo * 24 * 60 * 60 * 1000;
  const randomHours = (Math.random() - 0.5) * hoursVariation * 60 * 60 * 1000;
  return new Date(now - baseDaysAgo + randomHours).toISOString();
};

// Generate random like count based on post age and type
const generateLikes = (daysAgo: number, isSpecial: boolean = false) => {
  const base = Math.max(1, 50 - (daysAgo * 2)); // Newer posts get more likes
  const variation = Math.random() * 30;
  const special = isSpecial ? 40 : 0;
  return Math.floor(base + variation + special);
};

// Generate random comment count
const generateComments = (likes: number) => {
  return Math.floor(likes * 0.1 + Math.random() * 15);
};

